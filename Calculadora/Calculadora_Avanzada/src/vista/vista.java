    private void boton_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_igualActionPerformed
        try {
            segundo_dato = Float.parseFloat(this.pantalla_grande.getText());
            switch (operador) {
                case "+": {
                    resultado = primer_dato + segundo_dato;
                    if (resultado % 2 == 0) {
                        int cambio = (int) resultado;
                        this.pantalla_grande.setText(Integer.toString(cambio));
                        this.pantalla_pequena.setText(Integer.toString(cambio));
                    } else {
                        this.pantalla_grande.setText(Float.toString(resultado));
                        this.pantalla_pequena.setText(Float.toString(resultado));
                    }
                }
                break;
                case "-": {
                    resultado = primer_dato - segundo_dato;
                    if (resultado % 2 == 0) {
                        int cambio = (int) resultado;
                        this.pantalla_grande.setText(Integer.toString(cambio));
                        this.pantalla_pequena.setText(Integer.toString(cambio));
                    } else {
                        this.pantalla_grande.setText(Float.toString(resultado));
                        this.pantalla_pequena.setText(Float.toString(resultado));
                    }
                }
                break;
                case "*": {
                    resultado = primer_dato * segundo_dato;
                    if (resultado % 2 == 0) {
                        int cambio = (int) resultado;
                        this.pantalla_grande.setText(Integer.toString(cambio));
                        this.pantalla_pequena.setText(Integer.toString(cambio));
                    } else {
                        this.pantalla_grande.setText(Float.toString(resultado));
                        this.pantalla_pequena.setText(Float.toString(resultado));
                    }
                }
                break;
                case "/": {
                    resultado = primer_dato / segundo_dato;
                    if (resultado % 2 == 0) {
                        int cambio = (int) resultado;
                        this.pantalla_grande.setText(Integer.toString(cambio));
                        this.pantalla_pequena.setText(Integer.toString(cambio));
                    } else {
                        this.pantalla_grande.setText(Float.toString(resultado));
                        this.pantalla_pequena.setText(Float.toString(resultado));
                    }
                }
                break;
                case "%": {
                    resultado = primer_dato % segundo_dato;
                    if (resultado % 2 == 0) {
                        int cambio = (int) resultado;
                        this.pantalla_grande.setText(Integer.toString(cambio));
                        this.pantalla_pequena.setText(Integer.toString(cambio));
                    } else {
                        this.pantalla_grande.setText(Float.toString(resultado));
                        this.pantalla_pequena.setText(Float.toString(resultado));
                    }
                }
                break;
            }
        } catch (Exception e) {
            primer_dato = 0;
            this.pantalla_grande.setText("0");
            this.pantalla_pequena.setText("0");
        }
    }//GEN-LAST:event_boton_igualActionPerformed

