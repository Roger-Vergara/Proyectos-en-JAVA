    private void boton_borrar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_borrar2ActionPerformed
        String ultimo_digito_pantalla_grande = this.pantalla_grande.getText();
        String ultimo_digito_pantalla_pequena = this.pantalla_pequena.getText();

        if (ultimo_digito_pantalla_grande.length() >= 1
                && ultimo_digito_pantalla_pequena.length() >= 1) {

            int anterior_1 = ultimo_digito_pantalla_grande.length() - 1;
            int _final_1 = ultimo_digito_pantalla_grande.length();

            int anterior_2 = ultimo_digito_pantalla_pequena.length() - 1;
            int _final_2 = ultimo_digito_pantalla_pequena.length();

            this.pantalla_grande.replaceRange(null, anterior_1, _final_1);
            this.pantalla_pequena.replaceRange(null, anterior_2, _final_2);

        }
    }//GEN-LAST:event_boton_borrar2ActionPerformed

